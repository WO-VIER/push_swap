#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*pointer;
	t_list	*temp;

	if (lst == NULL || del == NULL)
		return ;
	pointer = *lst;
	while (pointer != NULL)
	{
		temp = pointer->next;
		del(pointer->content);
		free(pointer);
		pointer = temp;
	}
	*lst = NULL;
}
